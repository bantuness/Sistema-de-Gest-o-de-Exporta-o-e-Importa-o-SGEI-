--SUM = SOMA
SELECT SUM(VALORUNITARIO) AS SOMA FROM PRODUTOS 


--AVG = AVERAGE = MEDIA
SELECT AVG(VALORUNITARIO) AS AVERAGE FROM PRODUTOS

--MIN = MINIMO
SELECT MIN(VALORUNITARIO) AS MINIMO FROM PRODUTOS

--MAX = MAXIMO
SELECT MAX(VALORUNITARIO) AS MAXIMO FROM PRODUTOS

--% = REPRESENTAR O MODULO DE DIVISAO 
SELECT
ID_PRODUTO,
VALORUNITARIO,
VALORUNITARIO % 2 AS MODULOS
FROM PRODUTOS

--SQRT = RAIZ QUADRADA
SELECT
ID_PRODUTO,
VALORUNITARIO,
SQRT (VALORUNITARIO) AS RAIZ_QUADRADA
FROM PRODUTOS

