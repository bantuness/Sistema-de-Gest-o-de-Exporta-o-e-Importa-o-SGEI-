SELECT 
    C.Nome, --AS Cliente,
    SUM(I.Quantidade) AS TotalProdutosExportados
FROM ItensExportacao I
INNER JOIN Exportacoes E ON I.Id_Exportacao = E.Id_Exportacao
INNER JOIN Clientes C ON E.Id_Cliente = C.Id_Cliente
GROUP BY C.Nome;